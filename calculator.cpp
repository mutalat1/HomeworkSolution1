#include<iostream>
using namespace std;

int main() {
	double num1, num2;

    //inputs
	cin >> num1 >> num2;

	// operations
	cout << num1 << " + " << num2 << " = " << num1 + num2 << "\n";
	cout << num1 << " - " << num2 << " = " << num1 - num2 << "\n";
	cout << num1 << " / " << num2 << " = " << num1 / num2 << "\n";
	cout << num1 << " * " << num2 << " = " << num1 * num2 << "\n";
}
